<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
				kechengmingcheng: '',
				kechengleixing: '',
				keshi: '',
				xuefei: '',
				wanchengrenshu: '',
				kechengtupian: '',
				jiaolianbianhao: '',
				jiaolianxingming: '',
				userid: '',
				},
				kechengmingchengOptions: [],
				kechengmingchengIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   kechengmingcheng : false,
                                   kechengleixing : false,
                                   keshi : false,
                                   xuefei : false,
                                   wanchengrenshu : false,
                                   kechengtupian : false,
                                   jiaolianbianhao : false,
                                   jiaolianxingming : false,
                                   userid : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
			
			
			
			
			
			
			
			
			
		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.jiaolianbianhao = this.user.jiaolianbianhao
			this.ruleForm.jiaolianxingming = this.user.jiaolianxingming

			// 下2
			res = await this.$api.option(`jianshenkecheng`,`kechengmingcheng`,{});
			this.kechengmingchengOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`zongjiesuantongji`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='kechengmingcheng'){
					this.ruleForm.kechengmingcheng = obj[o];
					this.ro.kechengmingcheng = true;
					continue;
					}
					if(o=='kechengleixing'){
					this.ruleForm.kechengleixing = obj[o];
					this.ro.kechengleixing = true;
					continue;
					}
					if(o=='keshi'){
					this.ruleForm.keshi = obj[o];
					this.ro.keshi = true;
					continue;
					}
					if(o=='xuefei'){
					this.ruleForm.xuefei = obj[o];
					this.ro.xuefei = true;
					continue;
					}
					if(o=='wanchengrenshu'){
					this.ruleForm.wanchengrenshu = obj[o];
					this.ro.wanchengrenshu = true;
					continue;
					}
					if(o=='kechengtupian'){
					this.ruleForm.kechengtupian = obj[o];
					this.ro.kechengtupian = true;
					continue;
					}
					if(o=='jiaolianbianhao'){
					this.ruleForm.jiaolianbianhao = obj[o];
					this.ro.jiaolianbianhao = true;
					continue;
					}
					if(o=='jiaolianxingming'){
					this.ruleForm.jiaolianxingming = obj[o];
					this.ro.jiaolianxingming = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			// 下二随
			async kechengmingchengChange (e) {
				this.kechengmingchengIndex = e.target.value
				this.ruleForm.kechengmingcheng = this.kechengmingchengOptions[this.kechengmingchengIndex]
				let res = await this.$api.follow(`jianshenkecheng`, `kechengmingcheng`,{
					columnValue: this.ruleForm.kechengmingcheng
				});
				if(res.data.kechengleixing){
					this.ruleForm.kechengleixing = res.data.kechengleixing
				}
				if(res.data.keshi){
					this.ruleForm.keshi = res.data.keshi
				}
				if(res.data.xuefei){
					this.ruleForm.xuefei = res.data.xuefei
				}
			},

			// 多级联动参数




			kechengtupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.kechengtupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if(this.ruleForm.xuefei&&(!this.$validate.isIntNumer(this.ruleForm.xuefei))){
					this.$utils.msg(`学费应输入整数`);
					return
				}
				if(this.ruleForm.wanchengrenshu&&(!this.$validate.isIntNumer(this.ruleForm.wanchengrenshu))){
					this.$utils.msg(`完成人数应输入整数`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`zongjiesuantongji`, this.ruleForm);
				}else{
					await this.$api.add(`zongjiesuantongji`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

